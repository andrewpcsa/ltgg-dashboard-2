# -------------------------------------------------------------------
# Helpers
# -------------------------------------------------------------------
def price_on_or_after(series: pd.Series, dt: pd.Timestamp):
    s = series.dropna()
    v = s[s.index >= dt]
    return float(v.iloc[0]) if len(v) else None


def price_on_or_before(series: pd.Series, dt: pd.Timestamp):
    s = series.dropna()
    v = s[s.index <= dt]
    return float(v.iloc[-1]) if len(v) else None


BUY_TYPES = {"New Buy", "Addition"}


def compute_performance(df: pd.DataFrame, full_trades: pd.DataFrame,
                        prices: pd.DataFrame, mode: str,
                        end_date: pd.Timestamp | None, fixed_days: int | None):
    """For each trade in `df`, compute % return between the trade date and an
    appropriate end date. For New Buy / Addition trades whose position was
    later completely sold, the end date is capped at that subsequent Complete
    Sale, so performance reflects the realised hold period rather than
    extending past the exit."""

    # Look up subsequent Complete Sales per company from the FULL trade
    # history (not the filtered subset, which may exclude Complete Sales).
    cs = full_trades[full_trades["Transaction Type"] == "Complete Sale"]
    complete_sales_by_company: dict[str, list[pd.Timestamp]] = {
        name: sorted(grp["Earliest Trade Date"].tolist())
        for name, grp in cs.groupby("Instrument Name")
    }

    perfs, end_used = [], []
    for _, row in df.iterrows():
        name = row["Instrument Name"]
        trade_date = row["Earliest Trade Date"]

        if name not in prices.columns:
            perfs.append(np.nan); end_used.append(pd.NaT); continue
        series = prices[name]

        # Base end date per mode
        if mode == "Fixed days after trade":
            base_end = min(trade_date + pd.Timedelta(days=fixed_days),
                           prices.index.max())
        else:
            base_end = end_date

        # Cap Buys/Additions at the earliest subsequent Complete Sale of the
        # same name, if one exists.
        if row["Transaction Type"] in BUY_TYPES:
            future_cs = [d for d in complete_sales_by_company.get(name, [])
                         if d > trade_date]
            if future_cs:
                base_end = min(base_end, future_cs[0])

        sp = price_on_or_after(series, trade_date)
        ep = price_on_or_before(series, base_end)
        end_used.append(base_end)
        if sp and ep and sp > 0:
            perfs.append((ep / sp - 1) * 100)
        else:
            perfs.append(np.nan)
    return perfs, end_used


