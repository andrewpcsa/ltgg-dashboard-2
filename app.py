def _kpi_row(df: pd.DataFrame, label: str, n_total: int, is_sales: bool = False):
    st.markdown(f"**{label}** — {n_total} trade{'s' if n_total != 1 else ''}")
    c1, c2, c3, c4, c5 = st.columns(5)
    if len(df):
        c1.metric("Mean", f"{df['Performance %'].mean():+.0f}%")
        c2.metric("Median", f"{df['Performance %'].median():+.0f}%")
        c3.metric("% positive", f"{(df['Performance %'] > 0).mean()*100:.0f}%")
        # For sales, "best" means biggest avoided loss (most negative perf
        # after sale) and "worst" means biggest miss (most positive perf
        # after sale, i.e. sold something that kept rising).
        if is_sales:
            c4.metric("Best", f"{df['Performance %'].min():+.0f}%",
                      help="Biggest drop after the sale – your best-timed exit.")
            c5.metric("Worst", f"{df['Performance %'].max():+.0f}%",
                      help="Biggest rise after the sale – left the most on the table.")
        else:
            c4.metric("Best", f"{df['Performance %'].max():+.0f}%")
            c5.metric("Worst", f"{df['Performance %'].min():+.0f}%")
    else:
        for c in (c1, c2, c3, c4, c5):
            c.metric("—", "—")

