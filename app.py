@st.cache_data(show_spinner="Loading data…")
def load_data(pkl_path: str = PKL_PATH, _mtime: float | None = None):
    """`_mtime` is part of the cache key, so the cache is invalidated
    automatically whenever data.pkl is replaced."""
    with open(pkl_path, "rb") as f:
        return pickle.load(f)


def _pkl_mtime(pkl_path: str = PKL_PATH) -> float:
    p = Path(pkl_path)
    return p.stat().st_mtime if p.exists() else 0.0


